package com.tutorial.operators;

public class Operators {
    public static void main(String[] args) {
        System.out.println("=== Operators ===");

        int number1 = 12;
        int number2 = 6;

        // Addition
        //System.out.println(number1 + " + " + number2 + " = " + (number1 + number2));
        System.out.println(number1 + number2);

        // Subtraction
        System.out.println(number1 - number2);

        // Multiplication
        System.out.println(number1 * number2);

        // Division
        System.out.println(number1 / number2);

        // Modulus
        System.out.println(number1 % number2);
    }
}
