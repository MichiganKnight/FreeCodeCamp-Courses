package com.revature.weekThree.sql;

public class SQLLanguages {
    public static void main(String[] args) {
        System.out.println("=== SQL Sublanguages ===");

        System.out.println("SQL is Broken Down Into 5 Sublanguages:");
        System.out.println("DDL: Data Definition Language - Defines Data Structure");
        System.out.println("DML: Data Manipulation Language - Inserts, Updates, Deletes Records");
        System.out.println("DCL: Data Control Language - Grants or Revokes Access Permissions to Database Objects");
        System.out.println("TCL: Transaction Control Language - Defines Concurrent Operation Boundaries");
        System.out.println("DQL: Data Query Language - Searches, Filters, Groups, and Aggregates Stored Data");
    }
}
